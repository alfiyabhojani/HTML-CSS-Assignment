// 21. Check if the season is Autumn, Winter, Spring or Summer. If the user input is :
//     - September, October or November, the season is Autumn.
//     - December, January or February, the season is Winter.
//     - March, April or May, the season is Spring
//     - June, July or August, the season is Summer

let month = "July";

switch (month) {
  case "March":
    console.log("Spring");
    break;

  case "April":
    console.log("Spring");
    break;

  case "May":
    console.log("Spring");
    break;

  case "June":
    console.log("Summer");
    break;

  case "July":
    console.log("Summer");
    break;

  case "August":
    console.log("Summer");
    break;

  case "September":
    console.log("Autumn");
    break;

  case "October":
    console.log("Autumn");
    break;

  case "November":
    console.log("Autumn");
    break;

  case "December":
    console.log("Winter");
    break;

  case "January":
    console.log("Winter");
    break;

  case "February":
    console.log("Winter");
    break;
}
